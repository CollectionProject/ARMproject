/**-------------------------------------------------------
  * @函数名 SysTick_Handler_User
  * @功能   系统节拍定时器服务请求用户处理函数
  * @参数   无
  * @返回值 无
***------------------------------------------------------*/
void SysTick_Handler_User(void)
{
    static uint32_t TimeIncrease = 0;

    if((TimeIncrease%100) == 0)
    {
        if((TimeIncrease%2000) == 0) //每2秒亮100毫秒
        {
            LED4OBB = 0;
        }
        else
        {
            LED4OBB = 1;
        }
    }
    TimeIncrease++;
	
}

							


